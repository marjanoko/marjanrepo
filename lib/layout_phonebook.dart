          code: "PERMISSION_DENIED",
          message: "Access to location data denied",
          details: null);
    } else if (permissionStatus == PermissionStatus.disabled) {
      throw new PlatformException(
          code: "PERMISSION_DISABLED",
          message: "Location data is not available on device",
          details: null);
    }*/
  }

  @override
  Widget build(BuildContext context) {
    return Scaffold(
      appBar: new AppBar(
        title: Text("Contact List"),
        backgroundColor: Colors.green[HeaderMenuColor],// lebih satur
      ),
      backgroundColor: Colors.green[BackgroundColor],// lebih terangan dikit
      body: SafeArea(
        child: _contacts != null
            ? ListView.builder(
          itemCount: _contacts?.length ?? 0,
          itemBuilder: (BuildContext context, int index) {
            Contact c = _contacts?.elementAt(index);
            return ListTile(
              onTap: () {
                if(c.phones.length>0){
                  launch("tel://"+c.phones.elementAt(0).value);
                }
              },
              leading: (c.avatar != null && c.avatar.length > 0)
                  ? CircleAvatar(backgroundImage: MemoryImage(c.avatar))
                  : CircleAvatar(child: Text(c.initials())),
              title: Text(c.displayName ?? ""),
              subtitle: Text(c.phones.length>0 ? c.phones.elementAt(0).value:"-"),
            );
          },
        )
            : Center(child: CircularProgressIndicator(),),
      ),
    );
  }
}
